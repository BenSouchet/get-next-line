#include "libft.h"
#include <stdlib.h>
#include <string.h>

char	*ft_strdup(const char *s1)
{
	char	*dest;
	int		cpt;
	int		i;

	i = 0;
	cpt = ft_strlen(s1);
	if (!(dest = (char *)malloc(sizeof(char*) * cpt + 1)))
		return (NULL);
	while (s1[i] != '\0')
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
